package streams.desafio;

public class Carro {
    
    final String nome;
    final String marca;
    final String cor;
    final double potencia;
    final double preco;


    public Carro(String nome, String marca, String cor, double potencia, double preco) {
        this.nome = nome;
        this.marca = marca;
        this.cor = cor;
        this.potencia = potencia;
        this.preco = preco;
    }

}
